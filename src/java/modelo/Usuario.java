public class Usuario {
    String k_usuario;
    String c_usuario;
    String k_tipo;
    String n_usuario;
    String d_usuario;
    String i_usuario;

    public String getK_usuario() {
        return k_usuario;
    }

    public void setK_usuario(String k_usuario) {
        this.k_usuario = k_usuario;
    }

    public String getC_usuario() {
        return c_usuario;
    }

    public void setC_usuario(String c_usuario) {
        this.c_usuario = c_usuario;
    }

    public String getK_tipo() {
        return k_tipo;
    }

    public void setK_tipo(String k_tipo) {
        this.k_tipo = k_tipo;
    }

    public String getN_usuario() {
        return n_usuario;
    }

    public void setN_usuario(String n_usuario) {
        this.n_usuario = n_usuario;
    }

    public String getD_usuario() {
        return d_usuario;
    }

    public void setD_usuario(String d_usuario) {
        this.d_usuario = d_usuario;
    }

    public String getI_usuario() {
        return i_usuario;
    }

    public void setI_usuario(String i_usuario) {
        this.i_usuario = i_usuario;
    }
}
